//Crie una  funcion que recibe dos palavras (Strings) y retorna su concatenacion.
//De manera resumida, deve juntar las dos palabras.
function concatenacion(palabra1: string, palabra2: string) {
  return palabra1 + " " + palabra2;
}

const juntarPalabras = concatenacion("Facultadad", "Informatica");
